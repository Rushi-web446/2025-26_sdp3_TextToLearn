const getLessonContentService = async ({
  moduleId,
  lessonId,
}) => {
  const result =
    await findLessonForUser({
      moduleId,
      lessonId,
    });

  if (!result) {
    throw new Error("Lesson not found");
  }

  const lesson = result;

  return {
    lesson: {
      title: lesson.title,
      description: lesson.briefDescription,
      ...lesson.content,
      hinglishContent: lesson.hinglishContent,
      isCompleted: lesson.isCompleted,
      lessonIndex: lesson.lessonIndex,
      module: lesson.module,
      _id: lesson._id,
    },
    module: {
      title: "Learning Module", // Fallback title
      moduleIndex: lesson.moduleIndex || 0,
    },
  };
};

