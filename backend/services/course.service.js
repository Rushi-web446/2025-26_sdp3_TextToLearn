const getCourseDetailsWithProgressService = async (courseId, userId) => {
  if (!courseId || !userId) {
    throw new Error("Course ID and User ID are required");
  }

  const course = await findById(courseId); // now populates modules + lessons
  if (!course) {
    throw new Error("Course not found");
  }

  // Find first incomplete module
  let currentModuleIndex = 1;
  let currentLessonIndex = 1;

  let found = false;
  for (const mod of course.modules) {
    for (const lesson of mod.lessons) {
      if (!lesson.isCompleted) {
        currentModuleIndex = mod.moduleIndex;
        currentLessonIndex = lesson.lessonIndex;
        found = true;
        break;
      }
    }
    if (found) break;
  }

  // All lessons completed — point to the last lesson
  if (!found && course.modules.length > 0) {
    const lastMod = course.modules[course.modules.length - 1];
    currentModuleIndex = lastMod.moduleIndex;
    const lastLesson = lastMod.lessons[lastMod.lessons.length - 1];
    currentLessonIndex = lastLesson ? lastLesson.lessonIndex : 1;
  }

  return {
    course,
    progress: {
      currentModule: currentModuleIndex,
      currentLesson: currentLessonIndex,
    },
  };
};


